package domaciPetlje;

import java.util.Scanner;

public class DomaciWhile2 {

	public static void main(String[] args) {
		
		

		Scanner sc = new Scanner(System.in);
		int broj = sc.nextInt();
		int brojac = 1;
		
		
		
		
		while(brojac <= broj )  {
			
		brojac = brojac + 1;	
			
		System.out.println("Laku noc");
		}

	}

}
