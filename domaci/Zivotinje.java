package domaci;

public class Zivotinje { 
	
	private Vrsta vrsta;
	private String naziv;
	private String omiljenaHrana;
	public Zivotinje(Vrsta vrsta, String naziv, String omiljenaHrana) {
		super();
		this.vrsta = vrsta;
		this.naziv = naziv;
		this.omiljenaHrana = omiljenaHrana;
	}
	public Vrsta getVrsta() {
		return vrsta;
	}
	public void setVrsta(Vrsta vrsta) {
		this.vrsta = vrsta;
	}
	public String getNaziv() {
		return naziv;
	}
	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}
	public String getOmiljenaHrana() {
		return omiljenaHrana;
	}
	public void setOmiljenaHrana(String omiljenaHrana) {
		this.omiljenaHrana = omiljenaHrana;
	}
	
	
	
	
	
      
	
	
	
            
	
	
	
    
	
	
	
  
	
	
	

	

}
